import pandas as pd

# Load dataset
df = pd.read_csv("sales_data.csv")

# Display first 5 rows
print("ğŸ“Œ First 5 rows of the dataset:")
print(df.head())

# Dataset shape
print("\nğŸ“Š Dataset Shape (rows, columns):")
print(df.shape)

# Column names
print("\nğŸ§¾ Column Names:")
print(df.columns)

# Data types and basic info
print("\nâ„¹ï¸ Dataset Information:")
print(df.info())

# Basic statistics
print("\nğŸ“ˆ Basic Statistics:")
print(df.describe())

# ğŸ§¹ Data Cleaning

# Check missing values
print("\nâ“ Missing values before cleaning:")
print(df.isnull().sum())

# Remove duplicate rows
df = df.drop_duplicates()

# Fill missing values with 0 (simple & safe)
df = df.fillna(0)

print("\nâœ… Missing values after cleaning:")
print(df.isnull().sum())

# ğŸ“Š Data Analysis

total_sales = df['Total_Sales'].sum()
average_sales = df['Total_Sales'].mean()
max_sales = df['Total_Sales'].max()

print("ğŸ’° Total Sales:", total_sales)
print("ğŸ“Š Average Sales:", average_sales)
print("ğŸ”¥ Highest Sale:", max_sales)

import matplotlib.pyplot as plt

# Sales by Product
sales_by_product = df.groupby('Product')['Total_Sales'].sum()

sales_by_product.plot(kind='bar', title='Sales by Product')

plt.xlabel("Product")
plt.ylabel("Total Sales")
plt.show()

product_sales = df.groupby("Product")["Total_Sales"].sum()
plt.figure(figsize=(6, 6))
plt.pie(
    product_sales,
    labels=product_sales.index,
    autopct='%1.1f%%',
    startangle=140
)

plt.title("Sales Distribution by Product")
plt.show()